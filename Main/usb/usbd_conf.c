/**
  * @brief  Static single allocation.
  * @param  size: Size of allocated memory
  * @retval None
  */
void *USBD_static_malloc(uint32_t size)
{
	if (size == sizeof(USBD_CDC_HandleTypeDef))
	{
		return &cdc_handle;
	}
	else if (size == sizeof(USBD_VIDEO_HandleTypeDef))
	{
		return &video_handle;
	}

	return NULL;
}

